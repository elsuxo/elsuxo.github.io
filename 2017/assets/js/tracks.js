var polyline;

$(function() {
    // var myLatlng = new google.maps.LatLng(53.56536, 9.87491);
    var myLatlng = new google.maps.LatLng(53.5603303,9.8504456);
    var myStartFinish = new google.maps.LatLng(53.56536, 9.87491);
    var myOptions = {
        zoom: 14,
        center: myLatlng,
        mapTypeId: google.maps.MapTypeId.ROADMAP
    }
    var map = new google.maps.Map(document.getElementById("mapdisplay"), myOptions);
    polyline = new google.maps.Polyline({
        map: map,
        clickable: false,
        strokeColor: '#f7931d',
        strokeOpacity: 0.8,
        strokeWeight: 4,
        path: longpath
    });
    // var marker = new google.maps.Marker({
    //     position: myLatlng,
    //     map: map,
    //     title:"Landhaus-Flottbek"
    // });
    var image = 'https://developers.google.com/maps/documentation/javascript/examples/full/images/beachflag.png';
    var markerStart = new google.maps.Marker({
        position: myStartFinish,
        map: map,
        title:"Start/Ziel",
        icon: image
    });

    $('.displaylink').bind('click', function(){
        var mtype = $(this).attr('mtype');
        var path;
        if(mtype == 1) {
            path = shortpath;
            $('#nfin').show();
        } else {
            path = longpath;
            $('#nfin').hide();
        }

        polyline.setPath(path);
        var bounds = null;
        for(var i = 0; i < path.length; i++) {
            if(bounds == null) {
                bounds = new google.maps.LatLngBounds(path[i], path[i]);
            }
            bounds.extend(path[i]);
        }
        map.fitBounds(bounds);

        $('.displaylink').removeClass('active');
        $(this).addClass('active');
    });

    $('.active').click();

});
